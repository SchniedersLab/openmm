static void getBounds(double rho, int bounds[]) {
    double calculateIndex = (rho - MINIMUM_RADIUS) / SPACING;
    int below = (int) floor(calculateIndex);

    // std::cout << " getBounds rho " << rho << " index " << calculateIndex << " below " << below << std::endl;

    int above = below + 1;

    if (above >= NUM_POINTS) {
        // Extrapolate up from the top table values.
        below = NUM_POINTS - 1;
        above = NUM_POINTS - 2;
    } else if (below < 0) {
        // If below is less than 0, extrapolate down from the bottom table values.
        below = 0;
        above = 1;
    }

    bounds[0] = below;
    bounds[1] = above;
}

static double interpolate2D(double x1, double x2, double y1, double y2, double x, double y,
                     double fx1y1, double fx2y1, double fx1y2, double fx2y2) {
    double fxy1 = (x2 - x) / (x2 - x1) * fx1y1 + (x - x1) / (x2 - x1) * fx2y1;
    double fxy2 = (x2 - x) / (x2 - x1) * fx1y2 + (x - x1) / (x2 - x1) * fx2y2;

    return (y2 - y) / (y2 - y1) * fxy1 + (y - y1) / (y2 - y1) * fxy2;
}

void AmoebaReferenceGeneralizedKirkwoodForce::getNeckConstants(double rhoDescreened, double rhoDescreening, double constants[]) {
    // Convert the radii values from nm to A.
    rhoDescreened = rhoDescreened * 10.0;
    rhoDescreening = rhoDescreening * 10.0;

    // Determine low and high values for integration
    int boundsI[] = {0, 1};
    getBounds(rhoDescreened, boundsI);

    int boundsJ[] = {0, 1};
    getBounds(rhoDescreening, boundsJ);

    int lowI = boundsI[0];
    int highI = boundsI[1];
    int lowJ = boundsJ[0];
    int highJ = boundsJ[1];

    // Interpolate/Extrapolate Aij and Bij constant values
    double aij = interpolate2D(benchmarkedRadii[lowI], benchmarkedRadii[highI], benchmarkedRadii[lowJ], benchmarkedRadii[highJ],
                               rhoDescreened, rhoDescreening, Aij[lowI][lowJ], Aij[highI][lowJ], Aij[lowI][highJ], Aij[highI][highJ]);

    double bij = interpolate2D(benchmarkedRadii[lowI], benchmarkedRadii[highI],benchmarkedRadii[lowJ], benchmarkedRadii[highJ],
                               rhoDescreened, rhoDescreening, Bij[lowI][lowJ], Bij[highI][lowJ], Bij[lowI][highJ], Bij[highI][highJ]);

    // Never let Aij be negative.
    if (aij < 0.0) {
        aij = 0.0;
    }

    // Convert aij from Ang(-11) to nm(-11)
    constants[0] = aij * 1.0e11;
    // Convert bij from A to nm.
    constants[1] = bij * 0.1;
}

static double neckDescreen(double r, double radius, double radiusK, double sneck) {

    // Radius of water in nm.
    double radiusWater = 0.14;

    // If atoms are too widely separated there is no neck formed.
    if (r > radius + radiusK + 2.0 * radiusWater) {
        return 0.0;
    }

    // Get Aij and Bij based on parameterization by Corrigan et al.
    double constants[] = {0.0, 0.0};
    AmoebaReferenceGeneralizedKirkwoodForce::getNeckConstants(radius, radiusK, constants);
    double aij = constants[0];
    double bij = constants[1];

    // std::cout << " neckDescreen Aij " << aij << " Bij " << bij << std::endl;

    double rMinusBij = r - bij;
    double radiiMinusr = radius + radiusK + 2.0 * radiusWater - r;
    double power1 = rMinusBij * rMinusBij * rMinusBij * rMinusBij;
    double power2 = radiiMinusr * radiiMinusr * radiiMinusr * radiiMinusr;

    // Use Aij and Bij to get neck integral using Equations 13 and 14 from Aguilar/Onufriev 2010 paper
    // Sneck may be based on the number of heavy atoms bound to the atom being descreened.
    double neckIntegral = sneck * aij * power1 * power2;

    return neckIntegral;
}

void AmoebaReferenceGeneralizedKirkwoodForce::calculateGrycukBornRadii(const vector<Vec3> &particlePositions) {

    // Set the radius to 50 Angstroms (5 nm) if either the base radius is zero, or the
    // descreening integral is negative.
    const double MAX_RADIUS = 5.0;
    const double RECIP_MAX_RADIUS3 = pow(MAX_RADIUS, -3.0);
    const double PI4_3 = 4.0 / 3.0 * M_PI;
    const double INVERSE_PI4_3 = 1.0 / PI4_3;
    const double ONE_THIRD = 1.0 / 3.0;

    _bornRadii.resize(_numParticles);
    _soluteIntegral.resize(_numParticles);

    for (unsigned int ii = 0; ii < _numParticles; ii++) {

        if (_atomicRadii[ii] <= 0.0) {
            _bornRadii[ii] = MAX_RADIUS;
            continue;
        }

        double integralStartI = max(_atomicRadii[ii], _descreenRadii[ii]) + _dielectricOffset;

        double bornSum = 0.0;
        double neckSum = 0.0;
        for (unsigned int jj = 0; jj < _numParticles; jj++) {

            double sk = _descreenRadii[jj] * _scaleFactors[jj];

            if (ii == jj || integralStartI <= 0.0 || sk <= 0.0) continue;

            double xr = particlePositions[jj][0] - particlePositions[ii][0];
            double yr = particlePositions[jj][1] - particlePositions[ii][1];
            double zr = particlePositions[jj][2] - particlePositions[ii][2];

            double r2 = xr * xr + yr * yr + zr * zr;
            double r = sqrt(r2);

            // If atom ii engulfs the descreening atom, then continue.
            if (integralStartI > r + sk) continue;

            double sk2 = sk * sk;

            if ((integralStartI + r) < sk) {
                double lik = integralStartI;
                double uik = sk - r;
                double lik3 = lik * lik * lik;
                double uik3 = uik * uik * uik;
                bornSum -= (1.0 / uik3 - 1.0 / lik3);
            }

            double uik = r + sk;
            double lik;
            if ((integralStartI + r) < sk) {
                lik = sk - r;
            } else if (r < (integralStartI + sk)) {
                lik = integralStartI;
            } else {
                lik = r - sk;
            }

            double l2 = lik * lik;
            double l4 = l2 * l2;
            double lr = lik * r;
            double l4r = l4 * r;

            double u2 = uik * uik;
            double u4 = u2 * u2;
            double ur = uik * r;
            double u4r = u4 * r;

            double term =
                    (3.0 * (r2 - sk2) + 6.0 * u2 - 8.0 * ur) / u4r
                    - (3.0 * (r2 - sk2) + 6.0 * l2 - 8.0 * lr) / l4r;
            bornSum += term / 16.0;

            double mixedNeckScale = 0.5 * (_neckFactors[ii] + _neckFactors[jj]);
            if (mixedNeckScale > 0.0) {
                neckSum += neckDescreen(r, integralStartI, _descreenRadii[jj], mixedNeckScale);
            }
        }

        bornSum = bornSum * PI4_3 + neckSum;
        _soluteIntegral[ii] = bornSum;

        double baseRadiusI3 = _atomicRadii[ii] * _atomicRadii[ii] * _atomicRadii[ii];

        if (_tanhRescaling) {
            // Set up tanh function components
            double rhoi3Psi = baseRadiusI3 * _soluteIntegral[ii];
            double rhoi6Psi2 = rhoi3Psi * rhoi3Psi;
            double rhoi9Psi3 = rhoi6Psi2 * rhoi3Psi;
            // If the output of the tanh function is 1.0, then the Born radius will be MaxBornRadius
            double tanh_constant = PI4_3 * ((1.0 / baseRadiusI3) - RECIP_MAX_RADIUS3);
            bornSum = tanh_constant * tanh(beta0 * rhoi3Psi - beta1 * rhoi6Psi2 + beta2 * rhoi9Psi3);
        }

        bornSum = PI4_3 / baseRadiusI3 - bornSum;

        _bornRadii[ii] = (bornSum <= 0.0) ? MAX_RADIUS : pow(INVERSE_PI4_3 * bornSum, -ONE_THIRD);

        // Born radius must be at least as large as its base radius.
        if (_bornRadii[ii] < _atomicRadii[ii]) {
            _bornRadii[ii] = _atomicRadii[ii];
        }

        // Maximum Born radius is 50.0 Angstroms.
        if (_bornRadii[ii] > MAX_RADIUS) {
            _bornRadii[ii] = MAX_RADIUS;
        }
    }
}

